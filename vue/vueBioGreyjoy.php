					Asha Greyjoy<br/>
					<img class="img_bio" src="../style/images/Yara.png" alt="Yara">
				<td>
			  Asha Greyjoy (renommée Yara Greyjoy dans la série télévisée4) est la fille de Balon Greyjoy et la sœur aînée de Theon.
			  Elle a été élevée par Balon comme son héritière et commande son propre navire. Elle est très populaire parmi les Fers-Nés
		     et dotée d'un caractère bien trempé. Elle apparaît initialement en se faisant passer pour la femme d'un caréneur devant
			  son frère Theon, qu'elle n'a pas vu depuis dix ans, quand celui-ci revient dans les Îles de Fer et afin d'évaluer quel
			  homme il est devenu. Theon la courtise avec assiduité et est humilié publiquement lorsqu'il apprend sa véritable identité.
			  Lors de l'attaque des Fers-Nés contre la maison Stark, elle dirige l'attaque sur Motte-la-Forêt et tente vainement de 
			  convaincre son frère de quitter Winterfell avant que le piège ne se referme sur lui. Dans A Feast for Crows, elle fait 
			  partie avec ses deux oncles, Euron et Victarion, des principaux prétendants à la succession de son père et fuit les Îles
			  de Fer avec ses partisans lorsque Euron est finalement élu. Elle se réfugie à Motte-la-Forêt puis décide de partir pour 
			  rejoindre la mer et s'enfuir, lorsque le château se fait attaquer par les hommes de Stannis, et elle se fait capturer dans 
			  la forêt. Prisonnière de Stannis, elle est gardée comme otage par Alys Mormont. Des émissaires de Braavos arrivent au camp 
		     avec une jeune fille tremblotante ainsi que son frère Theon, qu'elle reconnaît à peine. L'actrice Gemma Whelan a été choisie
			  pour interpréter Asha dans la série télévisée.
