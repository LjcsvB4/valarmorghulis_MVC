		Balon Greyjoy est le seigneur des fer-nés et de la maison Greyjoy. Il gouverne sur les îles de fer en occupant le trône de grès.
	 Balon Greyjoy engagea une rébellion par le passé qui mena à une guerre contre le royaume des Sept Couronnes. Mis en échec par le
	  roi Robert Baratheon et son vassal Eddard Stark, on confia son unique fils Theon à la maison Stark en qualité de pupille pour
	  l'empêcher de se soulever à nouveau. Privé de son héritier, Balon rumina sa vengeance. Ce n'est que lorsque la guerre des cinq 
	 rois éclata que l'occasion se présenta à lui de se couronner à nouveau. Robb Stark, l'un des rois engagés dans le conflit de pouvoir
	 , envoya Theon (avec qui il est ami) à sa patrie natale pour se rallier aux fer-nés. Balon ignora simplement cette requête et se 
	contenta de mener ses propres razzias sur le royaume des Sept Couronnes. Déçu de ce que son fils Theon était devenu après son séjour
		 chez les Stark, il se moqua de lui et le renia presque, ce qui poussera Theon, en mal de reconnaissance, à trahir ses serments envers
	 Robb. Durant cette guerre, Balon trouve une mort assez étrange. Une tornade aurait balayé les îles de Pyk alors qu'il traversait un
	 pont suspendu, ce qui l'emporta dans une chute mortelle. Sa mort laisse place à une rivalité entre les fer-nés pour savoir qui prendra
   le pouvoir. L'acteur Patrick Malahide a été choisi pour interpréter Balon dans la série télévisée
