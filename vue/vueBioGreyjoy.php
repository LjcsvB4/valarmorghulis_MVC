	 		</th>
	 		<td>
		Theon Greyjoy est l'un des personnages de la saga Le Trône de fer écrite par George R. R. Martin. Il est le fils du seigneur 
		Balon Greyjoy des îles de fer et l'héritier légitime du trône de Grès.	 	
	 	À l'âge de dix ans, lors de l'échec de la rébellion Greyjoy, Theon devient "pupille" de lord Eddard Stark. En réalité,
	 	 il est otage des Stark. Le roi Robert Baratheon avait en effet choisi cette stratégie pour empêcher le roi Balon de 
	 	 tenter une nouvelle rébellion. Lors de ses années comme pupille, il ne se lie d'amitié qu'avec Robb Stark, le fils aîné 
	 	 de lord Eddard, du même âge que lui.
