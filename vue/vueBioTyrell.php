	 		<td>
	 		Loras Tyrell, dit « le Chevalier des fleurs », est le plus jeune fils de Mace Tyrell. Sa beauté et sa virtuosité à la lance 
	 		en font l'un des jeunes chevaliers les plus en vue des Sept Couronnes. Après la mort de Robert Baratheon, il rejoint la garde 
	 		personnelle de son frère Renly, et des soupçons courent sur une relation homosexuelle entre eux deux (l'homosexualité de Loras
	 		 et sa relation avec Renly seront plus tard confirmés par George R. R. Martin dans une interview12). À la mort de Renly, Loras, 
	 		 fou de douleur, tue deux de ses pairs qu'il considère comme responsables. Plus tard, il participe à la bataille de la Nera et 
	 		 est récompensé de ses prouesses en intégrant la Garde royale. Cersei, qui s'inquiète de son influence sur son jeune fils Tommen,
	 		  cherche à l'éloigner et l'envoie diriger le siège de Peyredragon. Loras s'empare de la forteresse mais est gravement blessé et 
	 		  défiguré par de l'huile bouillante versée sur son visage lors de l'assaut. L'acteur Finn Jones a été choisi pour interpréter Loras 
	 		  Tyrell dans l'adaptation télévisée.
