					<td>
					Tywin est le seigneur de Castral-Roc et par la même occasion, Gouverneur de l'Ouest. Fils du défunt Tytos Lannister,
				  il est dur et inflexible, surtout depuis la mort de sa femme, et possède une fortune colossale ainsi que de fortes 
				  armées ce qui lui donne la réputation d'être le plus puissant seigneur des Sept Couronnes. Il fut la Main du Roi Aerys
				  II le Dément avant de démissionner et de regagner ses terres. Il est le père de Cersei, Jaime et Tyrion, dit le Lutin,
				  qu'il déteste pour sa difformité et parce que sa naissance a coûté la vie à son épouse. Malgré cela, Tywin charge Tyrion
				  d'occuper le poste de Main du roi tandis que lui-même fait la guerre à Robb Stark. Mais, après un assaut manqué contre 
				  Vivesaigues, il finit par changer de tactique et retourne ses troupes contre celles de Stannis Baratheon. Avec l'aide de ses
				  alliés Tyrell, il triomphe de l'armée de Stannis lors de la bataille de la Nera. Dans A Storm of Swords, Tywin reprend 
	  			  alors pour lui-même le poste du Main du roi et devient le dirigeant effectif des Sept Couronnes. Lorsque Tyrion est accusé 
  				  d'avoir empoisonné Joffrey, c'est Tywin qui officie en tant que juge lors de son procès. Mais, la veille de l'exécution de
 				  Tyrion, celui-ci s'échappe de sa cellule et vient confronter son père. Celui-ci lui révèle alors qu'il a envoyé la première
				  épouse de Tyrion dans un bordel, et Tyrion le tue d'un carreau d'arbalète. L'acteur Charles Dance a été choisi pour interpréter 
				  Tywin Lannister pour la série télévisée.
					
